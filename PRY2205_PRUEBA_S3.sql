SELECT NRO_PROPIEDAD "PROPIEDAD",
       UPPER(DIRECCION_PROPIEDAD) "DIRECCION",
       TO_CHAR(VALOR_ARRIENDO, '$999G999G999') "ARRIENDO",
       TO_CHAR(VALOR_GASTO_COMUN, '$999G999G999') "GGCC_ACTUAL",
       LPAD(TO_CHAR(ROUND((VALOR_GASTO_COMUN * 1.10)), '$999G999G999'), 15) "GGCC_AJUSTADO",
       (CASE
            WHEN ID_COMUNA = 82 THEN 'Propiedad ubicada en comuna 82'
            WHEN ID_COMUNA = 84 THEN 'Propiedad ubicada en comuna 84'
            WHEN ID_COMUNA = 87 THEN 'Propiedad ubicada en comuna 87'
            ELSE 'No aplica'
        END) AS "UBICACION"
FROM PROPIEDAD
WHERE VALOR_ARRIENDO < &VALOR_MAXIMO AND NRO_DORMITORIOS IS NOT NULL AND ID_COMUNA IN (82,84,87)
ORDER BY VALOR_GASTO_COMUN ASC NULLS LAST, "ARRIENDO" DESC;


