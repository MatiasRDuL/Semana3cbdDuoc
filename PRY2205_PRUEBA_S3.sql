SELECT NRO_PROPIEDAD "Propiedad",
       NUMRUT_CLI "Código Arrendatario",
       LOWER(TO_CHAR(FECINI_ARRIENDO, 'DD.mon.YYYY')) "Fecha Inicio Arriendo",
       NVL(LOWER(TO_CHAR(FECTER_ARRIENDO)), 'PROPIEDAD ACTUALMENTE ARRENDADA') "Fecha Término Arriendo",
       LPAD(TO_CHAR(TRUNC(NVL(FECTER_ARRIENDO, SYSDATE) - FECINI_ARRIENDO), '999G999' ), 15) "Días Arriendo",
       ROUND((NVL(FECTER_ARRIENDO, SYSDATE) - FECINI_ARRIENDO) / 365) "Años Arriendo",
       (CASE
            WHEN ROUND((NVL(FECTER_ARRIENDO, SYSDATE) - FECINI_ARRIENDO) / 365) >= 10 THEN 'COMPROMISO DE VENTA'
            WHEN ROUND((NVL(FECTER_ARRIENDO, SYSDATE) - FECINI_ARRIENDO) / 365) BETWEEN 5 AND 9 THEN 'CLIENTE ANTIGUO'
            ELSE 'CLIENTE NUEVO'
        END) AS "Clasificación Estado"
FROM ARRIENDO_PROPIEDAD
WHERE (NVL(FECTER_ARRIENDO, SYSDATE) - FECINI_ARRIENDO) >= &PERIODO_X
ORDER BY TRUNC(NVL(FECTER_ARRIENDO, SYSDATE) - FECINI_ARRIENDO) DESC;

