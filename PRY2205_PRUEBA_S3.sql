SELECT ID_TIPO_PROPIEDAD "TIPO PROPIEDAD",
       (CASE
            WHEN ID_TIPO_PROPIEDAD = 'A' THEN 'Casa'
            WHEN ID_TIPO_PROPIEDAD = 'B' THEN 'Departamento'
            WHEN ID_TIPO_PROPIEDAD = 'C' THEN 'Local'
            WHEN ID_TIPO_PROPIEDAD = 'D' THEN 'Parcela sin casa'
            WHEN ID_TIPO_PROPIEDAD = 'E' THEN 'Parcela con casa'
            ELSE 'No aplica'
        END) AS "DESCRIPCION",
        LPAD(TO_CHAR(ROUND(AVG(VALOR_GASTO_COMUN)), '$999G999'), 25) "PROMEDIO GASTO COMUN",
        COUNT(NRO_PROPIEDAD) "CANTIDAD PROPIEDADES",
        LPAD(TO_CHAR(ROUND(AVG(NVL(VALOR_ARRIENDO, 0))), '$999G999G999'), 25) "PROMEDIO VALOR ARRIENDO"
FROM PROPIEDAD
GROUP BY ID_TIPO_PROPIEDAD
HAVING ROUND(AVG(VALOR_ARRIENDO)) >= &ARRIENDO_MINIMO
ORDER BY ID_TIPO_PROPIEDAD ASC, (ROUND(AVG(VALOR_ARRIENDO))) DESC;

